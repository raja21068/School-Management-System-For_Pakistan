/**
 * Keys of Gender in Student and Employees
 */
public class Gender {
    public static final String MALE = "M";
    public static final String FEMALE = "F";

    public static final String[] getGenders(){
        String[] str = {"Male","Female"};
        return str;
    }
    
    public static final String getCode(String s){
        if(s.trim().toLowerCase().equals("male")){
            return MALE;
        }
        return FEMALE;
    }
    
    public static final String getString(String code){
        if(code.trim().toLowerCase().equals("m")){
            return "Male";
        }
        return "Female";
    }

}
